import { SecretParam } from "firebase-functions/lib/params/types";

// GET ================================================================
export function getGeocodeRoute(
	encodedAddress: string,
	apiKey: SecretParam
): string {
	return `https://maps.googleapis.com/maps/api/geocode/json?address=${encodedAddress}&key=${apiKey.value()}`;
}

